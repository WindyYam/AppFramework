/* USER CODE BEGIN 4 */
static uint32_t GetRTCTimestamp()
{
    RTC_TimeTypeDef rtcTime;
    RTC_DateTypeDef rtcDate;
    (void)HAL_RTC_GetTime(&hrtc, &rtcTime, RTC_FORMAT_BIN);
    (void)HAL_RTC_GetDate(&hrtc, &rtcDate, RTC_FORMAT_BCD);
    return (rtcTime.Hours * 3600000U) + (rtcTime.Minutes * (uint32_t)60000U) + (rtcTime.Seconds * (uint32_t)1000U) + ((1000U * (rtcTime.SecondFraction - rtcTime.SubSeconds)) / (rtcTime.SecondFraction + 1U));
}

void AppFramework_SuspendHook(uint32_t timeInMs)
{   
    HAL_SuspendTick();
    const uint32_t clk = 2048;  // 2048HZ RTC wakeup clock (32768 / DIV16)
    uint32_t cnt = (timeInMs >= (UINT16_MAX * 1000U / clk))? UINT16_MAX : timeInMs * clk / 1000U;

    (void)HAL_RTCEx_SetWakeUpTimer_IT(&hrtc, cnt, RTC_WAKEUPCLOCK_RTCCLK_DIV16);
#ifdef USE_STOP1_MODE
    HAL_PWR_EnterSTOPMode(PWR_LOWPOWERREGULATOR_ON, PWR_STOPENTRY_WFI);
#else
    HAL_PWR_EnterSLEEPMode(PWR_LOWPOWERREGULATOR_ON, PWR_STOPENTRY_WFI);
    HAL_PWREx_DisableLowPowerRunMode();
#endif
    uint32_t nowRtc = GetRTCTimestamp();
    uint32_t rtcElapse;
    if(nowRtc < lastRtc)
    {
      //24 hours overflow, add it back
      rtcElapse = nowRtc + 24U * 3600000U - lastRtc;
    }
    else
    {
      rtcElapse = nowRtc - lastRtc;
    }
    rtcNowAccum += rtcElapse;
    uint32_t halTick = HAL_GetTick();
    uint32_t rtcDiff = rtcNowAccum - rtcInit;
    uint32_t halDiff = halTick - halInit;
    timestampCorrection = (rtcDiff > halDiff) ? rtcDiff - halDiff : 0U;
    // In some case timestampCorrection can go backward due to unsynchronized hal tick and rtc tick behaviour (1ms increment vs 4ms increment)
    // We should avoid that, and also take wrap around into consideration, judging by the fact that fusion RTC counter can sleep at most several seconds
    timestampCorrection = (timestampCorrection > lastTimestampCorrection) ? timestampCorrection : ((lastTimestampCorrection - timestampCorrection > UINT16_MAX)? timestampCorrection : lastTimestampCorrection);
    lastTimestampCorrection = timestampCorrection;
    lastRtc = nowRtc;
    HAL_ResumeTick();
}

void AppFramework_PostSuspendHook()
{
#ifdef USE_STOP1_MODE
    // Stop1 mode stops PLL, we need to re-initialize the clock
    SystemClock_Config();
#endif
}

uint32_t AppFramework_GetTimestampHook(void)
{
    return HAL_GetTick() + timestampCorrection;
}

